    'Units','normalized',...
    'Position',[0.9 0.80 0.08 .05],...
    'String','A',...
    'Callback',['A=','str2num(get(gco,''String''));trig(A,B,f,N,C,D);']);

uicontrol('Style','text',...
    'Units','normalized',...
    'Position',[0.9 0.63 0.08 .05],...
    'Backgroundcolor',[0.5 0.5 0.9],...
    'String','C');

