    'Units','normalized',...
    'Position',[0.9 0.50 0.08 .05],...
    'String','D',...
    'Callback',['D=','str2num(get(gco,''String''));trig(A,B,f,N,C,D);']);

uicontrol('Style','text',...
    'Units','normalized',...
    'Position',[0.9 0.73 0.08 .05],...
    'backgroundcolor',[0.5 0.5 0.9],...
    'string','B');

