function trig(A,B,f,N,C,D)
T=1/f; 
t=0:T/100:N*T; 
x1=A*sin(2*pi*f*t); 
x2=B*cos(10*2*pi*f*t); 
x3=x1-x2;
x4=C*tan(2*pi*f*t);
x5=D*tan(5*2*pi*f*t);
x6=x4-x5;


    
