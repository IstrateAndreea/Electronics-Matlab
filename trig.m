    'Units','normalized',...
    'Position',[0.9 0.30 0.08 .05],...
    'String','f',...
    'Callback',['f=','str2num(get(gco,''String''));trig(A,B,f,N,C,D)']);

uicontrol('Style','text',...
    'Units','normalized',...
    'Position',[0.9 0.43 0.08 .05],...
    'Backgroundcolor',[0.5 0.5 0.9],...
    'String','N');

uicontrol('Style','edit',...
    'Units','normalized',...
    'Position',[0.9 0.40 0.08 .05],...
    'String','N',...
    'Callback',['N=','str2num(get(gco,''String''));trig(A,B,f,N,C,D)']);

