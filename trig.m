    'Units','normalized',...
    'Position',[0.9 0.2 0.08 .05],...
    'String','Reset to Initial Values',...
    'FontName','Arial',...
    'Callback','trig(10,5,50,4,10,5);');

subplot('position',[0.07 0.72 0.3 0.20]);
plot(t,x1);
grid on;
title('Sinus ');
xlabel('time [s]');
ylabel('Amplitude [V]');

subplot('position',[0.07 0.38 0.3 0.20]);
plot(t,x2);
grid on;
title('Cosinus ');
xlabel('time [s]');
ylabel('Amplitude [V]');

subplot('position',[0.07 0.05 0.3 0.20]);
plot(t,x3);
grid on;
title('Difference 1 ');
xlabel('time [s]');
ylabel('Amplitude [V]');

subplot('position',[0.5 0.72 0.3 0.20]);
plot(t,x4);
grid on;
title('Tangent 1 ');
xlabel('time [s]');
ylabel('Amplitude [V]');

subplot('position',[0.5 0.38 0.3 0.20]);
plot(t,x5);
